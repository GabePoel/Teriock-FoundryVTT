import MechanicPseudoDocument from "../../abstract/mechanic-pseudo-document.mjs";

/**
 * @property {Teriock.Automations.Type} type
 * @property {ID<BaseAutomation>} _id
 */
export default class BaseAutomation extends MechanicPseudoDocument {
  /** @inheritDoc */
  static LOCALIZATION_PREFIXES = [...super.LOCALIZATION_PREFIXES, "TERIOCK.AUTOMATIONS.Base"];

  /** @inheritDoc */
  static get LABEL() {
    return "TERIOCK.AUTOMATIONS.Base.LABEL";
  }

  /**
   * @inheritDoc
   * @returns {{ documentName: "Automation", macro: boolean }}
   */
  static get metadata() {
    return Object.assign(super.metadata, {
      changes: false,
      crit: false,
      documentName: "Automation",
      label: _loc("TERIOCK.AUTOMATIONS.Base.LABEL"),
      macro: false,
      trigger: false,
    });
  }

  /**
   * Whether this is passive or not.
   * @returns {boolean}
   */
  get isPassive() {
    if (this.document.type === "ability") { return this.document?.system?.maneuver === "passive"; }
    return this.document.type === "property";
  }

  /**
   * Activations generated by this automation.
   * @param {Teriock.Automations.GetActivationsOptions} [_options]
   * @returns {Promise<Teriock.Activations.Any[]>}
   */
  async getActivations(_options = { execution: null, rollData: {} }) {
    return [];
  }

  /**
   * Changes generated by this automation.
   * @returns {Teriock.Changes.QualifiedChangeData[]}
   */
  getChanges() {
    return [];
  }
}
