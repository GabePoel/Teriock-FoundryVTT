import { localizeChoices } from "../../../../helpers/localization.mjs";
import { competenceField } from "../../../fields/helpers/builders.mjs";
import { PropagationDataMixin } from "../../../shared/mixins/_module.mjs";
import TypedPseudoDocument from "../../abstract/typed-pseudo-document.mjs";

const { fields } = foundry.data;

/**
 * @extends {Teriock.Automations.BaseAutomationData}
 * @extends {TypedPseudoDocument}
 * @mixes PropagationData
 * @property {ID<BaseAutomation>} _id
 * @property {Teriock.Automations.Type} type
 */
export default class BaseAutomation extends PropagationDataMixin(TypedPseudoDocument) {
  /** @inheritDoc */
  static LOCALIZATION_PREFIXES = [...super.LOCALIZATION_PREFIXES, "TERIOCK.AUTOMATIONS.Base"];

  /** @inheritDoc */
  static get LABEL() {
    return "TERIOCK.AUTOMATIONS.Base.LABEL";
  }

  /**
   * @inheritDoc
   * @returns {{ documentName: "Automation", macro: boolean }}
   */
  static get metadata() {
    return Object.assign(super.metadata, {
      changes: false,
      crit: false,
      documentName: "Automation",
      label: _loc("TERIOCK.AUTOMATIONS.Base.LABEL"),
      macro: false,
      trigger: false,
    });
  }

  /** @inheritDoc */
  static get TYPE() {
    return "base";
  }

  /** @inheritDoc */
  static defineSchema() {
    return Object.assign(super.defineSchema(), {
      competencies: new fields.SetField(competenceField(), {
        initial: [0, 1, 2],
      }),
      heighten: new fields.SetField(
        new fields.NumberField({
          choices: localizeChoices({
            0: "TERIOCK.AUTOMATIONS.Base.FIELDS.heighten.choices.0",
            1: "TERIOCK.AUTOMATIONS.Base.FIELDS.heighten.choices.1",
          }),
        }),
        {
          initial: [0, 1],
        },
      ),
    });
  }

  /**
   * Whether this is active and should be included in the overall effect.
   * @returns {boolean}
   */
  get active() {
    return this.competent;
  }

  /**
   * Whether this can crit.
   * @returns {boolean}
   */
  get canCrit() {
    return false;
  }

  /**
   * Whether the competence requirements for this to be active are met.
   * @returns {boolean}
   */
  get competent() {
    return this.competencies.has(this.parent.competence.value);
  }

  /**
   * Notification-style messages that appear in the editor form.
   * @returns {Teriock.UI.FormMessage[]}
   */
  get formMessages() {
    return [];
  }

  /**
   * Whether this is passive or not.
   * @returns {boolean}
   */
  get isPassive() {
    if (this.document.type === "ability") {
      return this.parent["maneuver"] === "passive";
    } else {
      return this.document.type === "property";
    }
  }

  /**
   * Whether this wants a competence dialog to be displayed upon execution.
   * @return {boolean}
   */
  get requiresCompetence() {
    return this.competencies.size !== 3;
  }

  /**
   * Activations generated by this automation.
   * @param {Teriock.Automations.GetActivationsOptions} [_options]
   * @returns {Promise<Teriock.Activations.Any[]>}
   */
  async getActivations(_options = { execution: null, rollData: {} }) {
    return [];
  }

  /**
   * Changes generated by this automation.
   * @returns {Teriock.Changes.QualifiedChangeData[]}
   */
  getChanges() {
    return [];
  }
}
